            El régimen au pair es un arreglo mediante el cual, una joven, de entre 17 y 30 años de edad, puede pasar una temporada en un 
            país extranjero para aprender el idioma y convivir como miembro de una familia del país elegido. La familia que la acoge deberá 
            asegurarle alojamiento, manutención y una pequeña remuneración en concepto de dinero de bolsillo. Dicha cantidad es de 70 Euros 
            por semana en España. La relación entre la joven en régimen au pair y la familia es absolutamente personal , y su éxito dependerá 
            de la voluntad que ponga la familia en ayudar a la joven a organizarse en sus estudios y distracciones, y la buena voluntad de la 
            joven para desempeñar sus quehaceres. Bajo ningún concepto deberá confundirse este acuerdo con el servicio doméstico normal.
